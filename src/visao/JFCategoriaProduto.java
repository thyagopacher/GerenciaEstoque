     private void listar() {
            tabela.setModel(new TableModel(titulo, obj.size(), titulo.length));
            for(int i=0; i < obj.size(); i++){
                    tabela.setValueAt(obj.get(i).getCodigo(), i, 0);
                    tabela.setValueAt(obj.get(i).getNome().toUpperCase(), i, 1);
            }
            tabela.setAutoCreateRowSorter(true);   
     }

     private void carregaCategoria() {
          obj_mestre = crn.procuraTodos(cx);
          if(obj_mestre != null && !obj_mestre.isEmpty()){
               ccategoria.removeAllItems();
               ccategoria.addItem("Nenhuma");
               for (CategoriaProduto cp : obj_mestre) {
                    ccategoria.addItem(cp.getNome().toUpperCase());
               }
          }
     }

     private void setaCategoria() {
          categoria.setNome(tnome.getText().toString());
          if(mestre != null && mestre.getCodigo() != null){
               categoria.setMestre(mestre);
          }
     }

     private void setaTelaCategoria() {
          tnome.setText(categoria.getNome());
          if(categoria != null && categoria.getMestre() != null && categoria.getMestre().getNome() != null){
               ccategoria.setSelectedItem(categoria.getMestre().getNome().toUpperCase());
          }else{
               ccategoria.addItem("Nenhuma");
          }
     }
