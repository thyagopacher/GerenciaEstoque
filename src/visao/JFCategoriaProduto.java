     private void bexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bexcluirActionPerformed
          String msg      = "";
          int    response = JOptionPane.showConfirmDialog(null, "Tem certeza disso,\n"
               + "Você quer excluir?", "Confirmação de excluir:",
               JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
          if (response == JOptionPane.YES_OPTION) {
               if(categoria != null && categoria.getCodigo() != null){
                    msg = crn.excluirCategoriaProduto(cx, categoria);
               }else{
                    msg = "Para excluir escolha antes uma categoria que tenha sido gravada no banco";
               }
          }
          if(msg != null && !msg.isEmpty()){
               JOptionPane.showMessageDialog(null, msg);
          }else{
               obj.remove(categoria);
               listar();
               JOptionPane.showMessageDialog(null, "Excluido com sucesso");
          }
     }//GEN-LAST:event_bexcluirActionPerformed

     private void bupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bupdateActionPerformed
          setaCategoria();
          String msg = crn.updateCategoriaProduto(cx, categoria);
          if(msg != null && !msg.isEmpty()){
               JOptionPane.showMessageDialog(null, msg);
          }else{
               for(int i = 0; i < obj.size(); i++){
                    if(obj.get(i).getCodigo() == categoria.getCodigo()){
                         obj.set(i, categoria);
                         listar();
                         break;
                    }
               }
               JOptionPane.showMessageDialog(null, "update com sucesso");
          }
     }//GEN-LAST:event_bupdateActionPerformed

     private void binserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_binserirActionPerformed
          categoria = new CategoriaProduto();
          setaCategoria();
          String msg = crn.inserirCategoriaProduto(cx, categoria);
          if(msg != null && !msg.isEmpty()){
               JOptionPane.showMessageDialog(null, msg);
          }else{
               obj.add(categoria);
               listar();
               JOptionPane.showMessageDialog(null, "Inserido com sucesso");
          }
     }//GEN-LAST:event_binserirActionPerformed

     private void ccategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ccategoriaActionPerformed
          if(obj_mestre != null && !obj_mestre.isEmpty() && !ccategoria.getSelectedItem().toString().equals("Nenhuma") && (ccategoria.getSelectedIndex() != -1)){
               mestre = obj_mestre.get(ccategoria.getSelectedIndex());
          }
     }//GEN-LAST:event_ccategoriaActionPerformed

