     private void listar() {
            tabela.setModel(new TableModel(titulo, obj.size(), titulo.length));
            for(int i=0; i < obj.size(); i++){
                    tabela.setValueAt(obj.get(i).getCodigo(), i, 0);
                    tabela.setValueAt(obj.get(i).getSite(), i, 1);
            }
            tabela.setAutoCreateRowSorter(true);   
     }

     private void setaLoja() {
          loja.setSite(tsite.getText());
          loja.setLinkftp(tftp.getText());
          loja.setLogin(tlogin.getText());
          loja.setSenha(tsenha.getText());
     }

     private void setaTelaLoja() {
           tsite.setText(loja.getSite());
           tftp.setText(loja.getLinkftp());
           tlogin.setText(loja.getLogin());
           tsenha.setText(loja.getSenha());
     }
