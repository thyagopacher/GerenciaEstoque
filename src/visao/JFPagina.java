    private void listar() {
            tabela.setModel(new TableModel(titulo, obj.size(), titulo.length));
            for(int i=0; i < obj.size(); i++){
                    tabela.setValueAt(obj.get(i).getCodigo(), i, 0);
                    tabela.setValueAt(obj.get(i).getTitulo().toUpperCase(), i, 1);
            }
            tabela.setAutoCreateRowSorter(true);    
    }  

     private void setaTelaPagina() {
          tnome.setText(pagina.getTitulo());
          tpalavrachave.setText(pagina.getPalavrachave());
          tconteudo.setText(pagina.getConteudo());
     }

     private void setaPagina() {
          pagina.setTitulo(tnome.getText());
          pagina.setPalavrachave(tpalavrachave.getText());
          pagina.setMetatitulo(pagina.getTitulo());
          pagina.setConteudo(tconteudo.getText());
     }

     private void limpar() {
          tnome.setText("");
          tpalavrachave.setText("");
          tconteudo.setText("");
     }
