     private void entrar() {
          String msg;
          try{
               msg = cx.conectarBanco();
               if(msg == null || msg.isEmpty()){
                    if(tlogin.getText() != null && !tlogin.getText().isEmpty() && tsenha.getText() != null && !tsenha.getText().isEmpty()){
                         usuario = urn.verificarLogin(cx, tlogin.getText(), tsenha.getText());
                         if(usuario != null && usuario.getCodigo() != null){
                              this.dispose();
                              new JFPrincipal(cx, usuario).setVisible(true);
                         }else{
                              msg = "Login ou senha errados, tente novamente";
                         }
                    }else{
                         msg = "Preencha login e senha para entrar";
                    }
               }
          }catch(Exception ex){
              msg = "Erro ao conectar o programa ocasionado por:\n" + ex; 
          }
          if(msg != null && !msg.isEmpty()){
               JOptionPane.showMessageDialog(null, msg);
          }
     }
